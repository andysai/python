# иә«й«ҳ
height = 168

# дҪ“йҮҚ
weight = height - 105

# иғёеӣҙпјқиә«й«ҳпјҲеҺҳзұіпјүГ—0.535
bust = height * 0.535

# и…°еӣҙпјқиә«й«ҳпјҲеҺҳзұіпјүГ—0.365
the_waist = height * 0.365

# иҮҖеӣҙпјқиә«й«ҳпјҲеҺҳзұіпјүГ—0.565
hipline = height * 0.565

print(f'иә«й«ҳ:{height}cm\n'
      f'дҪ“йҮҚ:{weight}KG\n'
      f'иғёеӣҙ:{round(bust,2)}cm\n'
      f'и…°еӣҙ:{round(the_waist,2)}cm\n'
      f'иҮҖеӣҙ:{round(hipline,2)}cm')